package me.gca.quadrato;

public class Quadrato {
    int x, y;
    int lato;

    public Quadrato(int x, int y, int lato) {
        this.x = x;
        this.y = y;
        this.lato = lato;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getLato() {
        return lato;
    }
}
